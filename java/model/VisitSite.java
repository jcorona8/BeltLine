package BeltLineApplication.java.model;

public class VisitSite {
    private String username;
    private String sName;
    private String visitSiteDate;

    public String getUsername() { return username; }

    public String getsName() { return sName; }

    public String getVisitSiteDate() { return visitSiteDate; }

    public void setUsername(String username) { this.username = username; }

    public void setsName(String sName) { this.sName = sName; }

    public void setVisitSiteDate(String visitSiteDate) { this.visitSiteDate = visitSiteDate; }
}
